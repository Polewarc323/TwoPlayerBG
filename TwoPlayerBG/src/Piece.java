

public class Piece {
    private String color;

	/***********************************************************************
	*
	* Get color method
	*
	* @return gets the piece color
	**********************************************************************/
    
    public String getColor() {
        return color;
    }

	/***********************************************************************
	*
	* Set color method
	*
	* @return sets the piece color
	**********************************************************************/
    
    public void setColor(String color) {
        this.color = color;
    }
}
